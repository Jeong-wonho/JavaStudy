package chap06;

public class FieldInitValueExample {
	public static void main(String[] args) {
		FieldInitValue fiv = new FieldInitValue();
		
		System.out.println("byteField: " + fiv.byteField);
		System.out.println("shortField: " + fiv.byteField);
		System.out.println("intField: " + fiv.byteField);
		System.out.println("longField: " + fiv.byteField);
		System.out.println("booleanField: " + fiv.byteField);
		System.out.println("charField: " + fiv.byteField);
		System.out.println("floatField: " + fiv.byteField);
		System.out.println("doubleField: " + fiv.byteField);
		System.out.println("arrField: " + fiv.byteField);
		System.out.println("referenceField: " + fiv.byteField);
	}
}
